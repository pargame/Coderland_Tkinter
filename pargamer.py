# tkinter: 파이썬에서 창(윈도우)과 버튼, 텍스트 같은 GUI(그래픽 사용자 인터페이스)를 만들게 해주는 도구입니다.
# 보통은 `tkinter`를 `tk`라는 별명으로 불러서 사용합니다.
import tkinter as tk


# 프로그램의 메인 함수입니다. 이 안에서 창을 만들고 보여주는 동작을 합니다.
def main():
	# root 변수는 우리의 '최상위 창'을 가리킵니다.
	# Tk()를 호출하면 실제로 화면에 보여질 새 창 객체를 하나 만듭니다.
	root = tk.Tk()

	# 창의 제목을 설정합니다. 창 맨 위의 막대에 표시되는 텍스트입니다.
	root.title("간단한 Tkinter 창")

	# 창의 초기 크기를 설정합니다. "가로x세로" 형식의 문자열을 사용합니다.
	# 여기서는 가로 400픽셀, 세로 200픽셀로 설정했습니다.
	root.geometry("400x200")

	# Label은 창에 텍스트나 이미지를 보여주는 위젯입니다.
	# 첫 번째 인자는 이 라벨의 부모(어디에 붙일지)인 root입니다.
	# text는 보여줄 글자, font는 글씨체와 크기를 정합니다.
	label = tk.Label(root, text="안녕하세요 — Tkinter 창입니다.")

	# pack()은 위젯을 창 안에 배치하는 방법 중 하나입니다.
	# expand=True는 남는 공간을 이 위젯이 차지하도록 돕습니다.
	label.pack(expand=True)

	# mainloop()는 프로그램을 실행 상태로 유지시켜 주는 부분입니다.
	# 이 줄이 있어야 창이 계속 뜨고 사용자의 입력(버튼 클릭 등)을 기다립니다.
	root.mainloop()


# 이 파일을 다른 파일에서 불러서 사용(import)할 수도 있고,
# 직접 이 파일을 실행할 수도 있습니다. 아래 조건은 "직접 실행할 때만" main()을 호출하게 합니다.
# 이렇게 하면 다른 코드에서 이 파일을 불러올 때 자동으로 창이 뜨지 않습니다.
if __name__ == "__main__":
	main()